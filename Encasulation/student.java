 class student {
	private String St_name;
	private int St_telno;
	private double St_gpa;
 
public void setname(String name)
{
this.St_name=name;
}
public void settelno(int tel)
{
this.St_telno=tel;
}
public void setgpa(double gpa)
{
this.St_gpa=gpa;
}


public String getname()
{
return this. St_name;
}

public int gettelno()
{
return this.St_telno;
}

public double getgpa()
{
return this.St_gpa;
}
}

